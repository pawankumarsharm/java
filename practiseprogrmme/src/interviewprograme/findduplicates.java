package interviewprograme;

public class findduplicates {

	public static void main(String[] args) {
		int ar[]= {1,2,3,9,5,6,1,4,6,9};
		for (int i = 0; i < ar.length; i++) {
			for (int j = i+1; j < ar.length; j++) {
				if(ar[i]==ar[j]&&(i!=j))
						{
						System.out.println(ar[j]);
						}
						
			}
		}
	}

}
